# Write a function, `square`, that has one parameter and returns its square.
def square(n):
	return n ** 2

if square(0) == 0:
	print('Thumbs up.')
else:
	print('Thumbs down.')

if square(1) == 1:
	print('Thumbs up.')
else:
	print('Thumbs down.')

if square(2) == 4:
	print('Thumbs up.')
else:
	print('Thumbs down.')

if square(3) == 9:
	print('Thumbs up.')
else:
	print('Thumbs down.')
